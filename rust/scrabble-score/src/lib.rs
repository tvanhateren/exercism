pub fn score(word: &str) -> u32 {
    word.chars().map(|x| get_char_value(x)).sum()
}

fn get_char_value(c: char) -> u32 {
    let c = c.to_ascii_uppercase();
    match c {
        'A' | 'E' | 'I' | 'O' | 'U' | 'L' | 'N' | 'R' | 'S' | 'T' => 1,
        'D' | 'G' => 2,
        'B' | 'C' | 'M' | 'P' => 3,
        'F' | 'H' | 'V' | 'W' | 'Y' => 4,
        'K' => 5,
        'J' | 'X' => 8,
        'Q' | 'Z' => 10,
        _ => 0,
    }
}
